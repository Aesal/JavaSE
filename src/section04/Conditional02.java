package section04;
/*
* if ~ else문
* 중괄호 생략 가능.
*
* */

public class Conditional02 {

    public static void main(String[] args) {
        int num = 13;

        if (num % 2 == 0) {
            System.out.println("짝수");
        } else {
            System.out.println("홀수");
        }

        if (num % 2 == 0)
            System.out.println("짝수");
         else
            System.out.println("홀수");



    }




}
