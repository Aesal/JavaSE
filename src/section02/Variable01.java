package section02;

/*
 * 1. 논리형 boolean; true or false
 * 
 * 
 * 
 */

public class Variable01 {
	public static void main(String[] args) {
		boolean isBool;
		
		isBool = true;
		
		System.out.println("논리형 변수에 저장된 값:" + isBool);
		
		isBool = false;
		System.out.println("논리형 변수 변경된 값 : " + isBool);
		
		
	}
}
