package section02;

public class Variable02 {
	
	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println("char 형 변수에 저장된 문자 : " + ch1);
		
		char ch2, ch3;
		
		ch2 = '金';
		ch3 = '볽';
		
		System.out.println(ch2);
		System.out.println(ch3);
		
		int num = ch1;
		System.out.println(num);
		
		char ch4 = 66;
		
		System.out.println(ch4);
	}

}
