package section03;
/*
* 시프트 연산자
* */
public class Operator07 {

    public static void main(String[] args) {

        int num = 10;
        int leftNum = num << 2;
        int rightNum = num >> 1;

        System.out.println("Org Num : " + num);
        System.out.println("Left Shift Num by 2 : " + leftNum);
        System.out.println("Right Shift Num by 1 : " + rightNum);






    }
}
