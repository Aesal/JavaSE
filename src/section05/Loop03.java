package section05;
/*
* for 문
* 초기식, 조건식, 증감식을 이용하여 반복하는 명령어
*
* */
public class Loop03 {

    public static void main(String[] args) {
        for (int i = 0; i < 100; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i < 100) {
            System.out.println(i);
            i++;
        }


    }

}
